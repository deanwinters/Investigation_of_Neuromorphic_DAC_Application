RESOLUTION = 8



def filter_cols(dataframe):

  # Hard code the column names which contain the pertinent marker voltage data.
  # Relies on a RESOLUTION param defined up top (since the no of markers is dependent on the voltage levels, i.e.: DAC resolution)
  # Then filter out irrelevant columns
  cols = []

  for i in range(1,RESOLUTION+2): #e.g.: 8 bit will have 9 markers including min and max Vout... V1 through V9
    cols.append(f"V{i}")

  return dataframe[cols]
