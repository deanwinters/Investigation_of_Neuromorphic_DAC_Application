def extract_t_v(dataframe):
  # Procures each marker(time, Voltage) value

  idx = [] # not used yet in any real capacity, is just included as good practice to append a key for each data row (or sanity check on number of markers/resolution)
  t = [] # time [s] at each marker
  Vout = [] # Voltage at V_out [V] at each marker

  for i in dataframe:
    col = dataframe[i]

    # Get time values from first row
    #np.float(64) check stops some reading error
    if isinstance(col[0], np.float64):
      t.append(col[0])

    # Sometimes Cadence format exports the voltage marker readouts on second row, but sometimes this is blank and read on third row, have a simple check
    # to see if empty (nan), then skip that second row and go to the third instead
    if isinstance(col[1], np.float64) and not np.isnan(col[1]):
      Vout.append(col[1])
    else:
      Vout.append(col[2])

  # Map the indices
  for i in range(0,len(t)):
    idx.append(i)

  return idx, t, Vout

