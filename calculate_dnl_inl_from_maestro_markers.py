def calculate_DNL_INL(idx, t, Vout):
  # Calculations

  # Ideal step size is just the aggregated net difference
  lsb_ideal = (Vout[-1] - Vout[0]) / (len(Vout) - 1)

  # Actual steps between each marker
  step = [Vout[i] - Vout[i-1] for i in range(1,len(Vout))]

  # DNL: ideal and observed steps differential [LSB]
  DNL = [(s-lsb_ideal) / lsb_ideal for s in step]

  # INL: cumsum of DNL at each marker
  INL = [sum(DNL[:i+1]) for i in range(len(DNL))]

  #print("LSB ideal:", lsb_ideal)
  #print("Steps:", step)
  #print("DNLs:", DNL)
  #print("INLs:", INL)
  #print("\n")

  # Or given as a max for reporting:
  DNL_max = float(max(abs(val) for val in DNL))
  INL_max = float(max(abs(val) for val in INL))

  print("Max DNL = ", DNL_max)
  print("Max INL = ", INL_max)

  return DNL_max, INL_max




def execution_helper(filename):
  # Bundle together in a single executable func for easier comparison between maestro sims

  print(f"\n FOR FILE: {filename} \n")
  df = pd.read_csv(filename)

  markers_df = filter_cols(df)

  index, time, voltage = extract_t_v(markers_df)

  DNL, INL = calculate_DNL_INL(index, time, voltage)

  return DNL, INL




